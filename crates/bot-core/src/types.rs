/// Get current timestamp in milliseconds
#[cfg(not(feature = "wasm"))]
pub fn now_ms() -> i64 {
    std::time::SystemTime::now()
        .duration_since(std::time::UNIX_EPOCH)
        .unwrap()
        .as_millis() as i64
}

/// Get current timestamp in milliseconds (WASM version using js_sys)
#[cfg(feature = "wasm")]
pub fn now_ms() -> i64 {
    js_sys::Date::now() as i64
}
