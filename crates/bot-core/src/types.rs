/// Price (fixed-point decimal)
#[derive(Debug, Clone, Copy, PartialEq, Eq, PartialOrd, Ord, Serialize, Deserialize)]
pub struct Price(pub Decimal);

impl Price {
    pub fn new(d: Decimal) -> Self {
        Self(d)
    }

    pub fn from_str(s: &str) -> Result<Self, rust_decimal::Error> {
        Ok(Self(s.parse()?))
    }

    pub fn value(&self) -> Decimal {
        self.0
    }

    /// Round to tick size
    pub fn round_to_tick(&self, tick_size: Decimal) -> Self {
        if tick_size <= Decimal::ZERO {
            return *self;
        }
        let ticks = (self.0 / tick_size).round();
        Self(ticks * tick_size)
    }

    /// Trim to N significant digits (Hyperliquid requires max 5)
    pub fn trim_to_sig_figs(&self, digits: u32) -> Self {
        if self.0.is_zero() {
            return *self;
        }

        // Get the scale (number of decimal places)
        let s = format!("{}", self.0.abs());
        let parts: Vec<&str> = s.split('.').collect();

        // Count significant digits
        let int_part = parts[0].trim_start_matches('0');
        let sig_digits_in_int = int_part.len();

        if sig_digits_in_int >= digits as usize {
            // Integer part has enough sig figs, round there
            let scale = sig_digits_in_int - digits as usize;
            let factor = Decimal::new(10_i64.pow(scale as u32), 0);
            let rounded = (self.0 / factor).trunc() * factor;
            return Self(rounded);
        }

        // Need to include decimal places
        let decimal_scale = digits as usize - sig_digits_in_int;
        let rounded = self.0.round_dp(decimal_scale as u32);
        Self(rounded)
    }
}

impl fmt::Display for Price {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        write!(f, "{}", self.0)
    }
}

impl From<Decimal> for Price {
    fn from(d: Decimal) -> Self {
        Self(d)
    }
}

/// Quantity (fixed-point decimal)
#[derive(Debug, Clone, Copy, PartialEq, Eq, PartialOrd, Ord, Serialize, Deserialize)]
pub struct Qty(pub Decimal);

impl Qty {
    pub fn new(d: Decimal) -> Self {
        Self(d)
    }

    pub fn from_str(s: &str) -> Result<Self, rust_decimal::Error> {
        Ok(Self(s.parse()?))
    }

    pub fn value(&self) -> Decimal {
        self.0
    }

    pub fn is_zero(&self) -> bool {
        self.0.is_zero()
    }

    /// Round to lot size (standard rounding - suitable for buy orders)
    pub fn round_to_lot(&self, lot_size: Decimal) -> Self {
        if lot_size <= Decimal::ZERO {
            return *self;
        }
        let lots = (self.0 / lot_size).round();
        Self(lots * lot_size)
    }

    /// Truncate to lot size (floor - suitable for sell orders to avoid overselling)
    pub fn trunc_to_lot(&self, lot_size: Decimal) -> Self {
        if lot_size <= Decimal::ZERO {
            return *self;
        }
        let lots = (self.0 / lot_size).trunc();
        Self(lots * lot_size)
    }
}

impl fmt::Display for Qty {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        write!(f, "{}", self.0)
    }
}

impl From<Decimal> for Qty {
    fn from(d: Decimal) -> Self {
        Self(d)
    }
}

impl std::ops::Add for Qty {
    type Output = Self;
    fn add(self, rhs: Self) -> Self::Output {
        Self(self.0 + rhs.0)
    }
}

impl std::ops::Sub for Qty {
    type Output = Self;
    fn sub(self, rhs: Self) -> Self::Output {
        Self(self.0 - rhs.0)
    }
}

impl std::ops::AddAssign for Qty {
    fn add_assign(&mut self, rhs: Self) {
        self.0 += rhs.0;
    }
}

