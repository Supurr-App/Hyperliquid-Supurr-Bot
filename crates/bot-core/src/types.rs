/// Position side (for display/logic)
#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]
pub enum PositionSide {
    Long,
    Short,
    Flat,
}

/// Position for an instrument (signed qty: positive=long, negative=short)
#[derive(Debug, Clone, Default, Serialize, Deserialize)]
pub struct Position {
    pub qty: Decimal, // signed: positive=long, negative=short
    pub avg_entry_px: Option<Price>,
    pub unrealized_pnl: Option<Decimal>,
    /// Accumulated realized PnL from closed trades
    #[serde(default)]
    pub realized_pnl: Decimal,
    /// Accumulated fees paid
    #[serde(default)]
    pub total_fees: Decimal,
}

impl Position {
    pub fn side(&self) -> PositionSide {
        if self.qty > Decimal::ZERO {
            PositionSide::Long
        } else if self.qty < Decimal::ZERO {
            PositionSide::Short
        } else {
            PositionSide::Flat
        }
    }

    pub fn is_flat(&self) -> bool {
        self.qty.is_zero()
    }

    pub fn abs_qty(&self) -> Decimal {
        self.qty.abs()
    }

    /// Compute total PnL: realized + unrealized - fees
    pub fn current_pnl(&self) -> Decimal {
        self.realized_pnl + self.unrealized_pnl.unwrap_or_default() - self.total_fees
    }
}

