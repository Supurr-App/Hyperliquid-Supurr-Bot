/// Instrument metadata (provided by you, not discovered)
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct InstrumentMeta {
    pub instrument_id: InstrumentId,
    pub market_index: MarketIndex,
    pub base_asset: AssetId,
    pub quote_asset: AssetId,
    pub tick_size: Decimal,
    pub lot_size: Decimal,
    pub min_qty: Option<Decimal>,
    pub min_notional: Option<Decimal>,
    pub fee_asset_default: Option<AssetId>,
    pub kind: InstrumentKind,
}

impl InstrumentMeta {
    /// Round a price to the instrument's tick size
    pub fn round_price(&self, price: Price) -> Price {
        price.round_to_tick(self.tick_size).trim_to_sig_figs(5)
    }

    /// Round a quantity to the instrument's lot size
    pub fn round_qty(&self, qty: Qty) -> Qty {
        qty.round_to_lot(self.lot_size)
    }

    /// Truncate a quantity DOWN to the instrument's lot size (floor)
    /// Use this for sell orders to avoid selling more than held
    pub fn trunc_qty(&self, qty: Qty) -> Qty {
        qty.trunc_to_lot(self.lot_size)
    }
}

